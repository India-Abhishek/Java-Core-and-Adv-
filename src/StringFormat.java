public class StringFormat {
    public static void main(String[] args) {
        // String.format() is a method in Java that allows you to create formatted strings by substituting placeholders with values.
        String str = String.format("Hello, %s! Welcome to %s", "Abhishek","New World");
        // palceholder '%s' holds the place in string to format.
        System.out.println(str);

    }
    
}
